public class rectangle {
	public static void main (String[] args) {
		
		double length;
		double width;
		double area;
		double perimeter;

		length=4;
		width=2;
		area=length*width;
		perimeter=length*2+width*2;

		System.out.println ("The area of the rectangle is "+area);
		System.out.println ("The perimeter of the rectangle is "+perimeter);
	}
}